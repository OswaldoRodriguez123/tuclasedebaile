                    <h3>Aprender a bailar</h3> 

                    <p>Nuestra organización tiene como misión principal, acompañar a cada uno de nuestros alumnos al logro de sus objetivos.</p> 

                    <p>Para esto hemos diseñado un programa de aprendizaje que incluye principalmente, el baile de la bachata y la salsa al estilo casino.</p>

                    <p>Géneros que se han popularizado hoy día y que ocupan un espacio de mucha relevancia y envergadura social.</p> 

                    <p>Muchas son las personas que han sido estigmatizadas desde niños, ya que siempre les han dicho que nacieron con dos pies izquierdos, que no tienen oído musical, o loro viejo no aprende a hablar. Lo peor y lamentable de este hecho es que, muchos han cedido ante esas poderosas aseveraciones sociales, asumiéndolas como ciertas y generando temor y un sentimiento de imposibilidad, suprimiendo los sentimientos y la auto confianza de las personas en sí mismas.</p> 

                    <p>En nuestra academia de baile comprendemos el impacto social que implica para cualquier persona no tener las habilidades necesarias para desenvolverse en la pista de baile.</p>

                    <p>Creemos que cada uno debe ser tratado y atendido de manera particular midiendo su crecimiento a través de diagnósticos y valoraciones de manera recurrente, tal cual lo hacemos a través del departamento llamado, coordinación de pista.</p>  

                    <p>Desde allí nos permitimos hacer el seguimiento y la supervisión a los bailadores, para así tener una métrica real y tangible del aprendizaje de nuestros alumnos.</p> 

                    <h3>Aprender a bailar Salsa</h3> 

                    <p>Para muchas personas que carecen de habilidades locomotoras o de disociación de movimientos, la salsa pudiera representar uno de los estilos de baile que tiene como característica especial, un grado de complejidad alto.</p>

                    <p>La buena noticia, es que hoy día contamos con métodos de baile que hace que cada alumno logre avanzar en sus metas y objetivos, llevando de forma continua y progresiva sus avances en la academia.</p> 

                    <p>En TU CLASE DE BAILE asumimos este tema con seriedad, entendiendo que para el equipo de instructores y colaboradores, es primero el compromiso y aprendizaje de nuestros alumnos y atención que puedan recibir, que cualquier otro elemento relevante de la academia, pero sin duda no con el mismo grade de prioridad y atención.</p> 

                    <p>Convirtiéndose en un trabajo en equipo, por un lado, la academia asumiendo el tema con total atención y por otro lado nuestros alumnos se comprometen a sumar su cuota de entrega para que juntos podamos lograr los objetivos trazados.</p> 

                    <p>Sabemos que la salsa en especial el estilo casino, es un género que, al momento de dominarlo correctamente con una buena ejecución de figuras, buen oído musical y una excelente ejecución del paso básico llamado “dile que no” pone en ventaja a cualquier bailador en una fiesta o en un evento social.</p> 

                    <h3>Pasos de baile de la salsa</h3>

                    <p>Los pasos serán orientados a través de una programación que inicia desde el básico hasta el avanzado, cada uno de estos pasos conllevan un nombre en específico, por citar algunos ejemplos:  ángulos, arriba, abajo, al centro, adiós, montaña, sombrero, el dedo setenta entre otros.</p>

                    <h3>Aprender a bailar bachata</h3>

                    <p>Otro género de baile que no puede faltar en la destreza social y particular de toda persona, es la bachata</p>

                    <p>Un género musical bailable que ha tomado un espacio de mucha relevancia en nuestra sociedad.</p>

                    <p>Artistas como Juan Luis Guerra, Romeo Santos y Price Royce, por citar algunos ejemplos, han contribuido a través de sus canciones, con la expansión y popularidad del género musical, cada día son más las personas que se interesen por disfrutar de este maravillo estilo de baile proveniente desde la isla de república dominicana</p>

                    <p>Con su base de ocho tiempos, que se identifica de manera fácil y con un paso básico atendiendo el “beat” (batida musical) hace que cualquier persona pueda aprender fácilmente bailar bachata.</p> 

                    <p>La bachata en su evolución cuenta con diversos estilos al momento de bailarse, tales como: la dominicana, la sensual, comercial, la bachata moderna, la urbana y hasta una mezcla o fusión de tango con bachata, llamada TangoBachata</p>

                    <p>En nuestra academia promovemos 3 estilos distintos, los cuales son enseñados durante las diversas programaciones que ofrecemos.</p> 

                    <p> 
                        <i class="fa fa-check color-azul"></i> La bachata comercial <br> 
                        <i class="fa fa-check color-azul"></i> La bachata sensual <br>
                        <i class="fa fa-check color-azul"></i> La bachata dominicana <br>
                    </p> 

                    <h3>Pasos de baile de bachata</h3>

                    <p>La bachata se baila en pareja y consta de una ejecución base en el que se incluye movimientos de caderas, mucho más pronunciado para la mujer que para el hombre.</p> 

                    <p>Generando figuras atractivas y vueltas interesante mientras se marca el paso básico de la bachata.</p> 

                    <p>Algunas vueltas, pasos y movimientos que podemos mencionar son:</p> 

                    <p> 
                        <i class="fa fa-check color-azul"></i> Ajuste <br> 
                        <i class="fa fa-check color-azul"></i> Básico lateral <br>
                        <i class="fa fa-check color-azul"></i> Bote <br>
                        <i class="fa fa-check color-azul"></i> El uno <br>
                        <i class="fa fa-check color-azul"></i> Enlace abierto y cerrado <br>
                        <i class="fa fa-check color-azul"></i> Giros <br>
                        <i class="fa fa-check color-azul"></i> Piquete <br>
                    </p>

                    <p>De esta forma se crean una serie de movimientos, aprendiendo de manera efectiva y progresiva. Con una programación diseñada, para que nuestros alumnos pueden aprender a bailar paso a paso.</p> 

                    <h3>Método de baile</h3>

                    <p>Es la verdadera clave y el arma poderosa con la que contamos en la escuela para enseñar a nuestros alumnos a lograr sus objetivos.</p> 

                    <p>Para nosotros es de gran relevancia y nos tomamos muy en serio el <a href="https://www.importancia.org/metodos-de-investigacion.php">método y la investigación</a>, como herramienta fundamental para el aprendizaje de nuestros alumnos.</p> 

                    <p>Resulta fácil asumir el compromiso de la enseñanza de cualquier disciplina siempre y cuando se tenga la experiencia, los elementos, herramientas y los métodos necesarios para lograrlo.</p> 

                    <p>Por ejemplo, al momento de aprender a manejar un vehículo, esta situación pudiera resultar para muchos una experiencia de gran complejidad, sin embargo, si cuentas con una buena orientación y disciplina al momento de la praxis, lograrás sin ningún problema aprender a manejar.</p>

                    <p>Algo parecido sucede con el baile, cuando entras en contacto con otro bailador(a) se genera una responsabilidad y un compromiso de dos (2).</p>

                    <p>En ese preciso momento la persona pudiera percibir esta actividad como una experiencia desagradable, el sentir no tener el control de la situación, por no poseer habilidades que le permitan desarrollarse en la pista de baile.</p>

                    <p>En ese preciso momento juega un rol fundamental la técnica y el método que usamos para el aprendizaje de cada uno de los pasos de baile que se ejecutan en la academia.</p>

                    <p>Desde un buen agarre, hasta movimientos, vueltas, giros y desplazamientos que te llevarán a lograr el resultado que como bailador esperas obtener en nuestra academia de baile.</p>  

                    <h3>Paso a paso</h3>

                    <p>Al momento de tomar la decisión e iniciar con la actividad danzaría, es importante destacar que abordaremos las secuencias paso a paso, instruyendo de manera precisa y ordenada, tal cual, se encuentra asignada en la programación de la salsa y bachata.</p> 

                    <p>Si el alumno no posee las destrezas básicas, no debe preocuparse debido a que en nuestra ejecución y procesos llevaremos una secuencia de aprendizaje uniforme y en modo ascendente.</p>

                    <p>En casos especiales que nuestros alumnos presenten dificultades al momento del aprendizaje, o hayan perdido diversas clases ,abordaremos con inmediatez dichos casos, para luego ser tratado a través de clases personalizadas o una atención especial que enruten al alumno a lograr sus objetivos.</p> 

                    <p>Por tal motivo no existe razones para negarse la oportunidad de aprender a bailar</p> 

